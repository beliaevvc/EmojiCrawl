/**
 * Clock port (Domain dependency).
 *
 * Слой: Domain (порт).
 *
 * ## Зачем
 * Доменная логика не должна напрямую вызывать `Date.now()`, потому что:
 * - время становится “скрытой” глобальной зависимостью,
 * - это ломает повторяемость и усложняет тесты/реплеи,
 * - это мешает сериализации событий (когда хотим контролировать таймстемпы).
 *
 * Поэтому домен принимает Clock как зависимость (инъекция через application/app).
 */
export type Clock = {
  /** Возвращает текущее время в миллисекундах. */
  now: () => number;
};


