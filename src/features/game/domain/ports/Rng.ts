/**
 * RNG port (Domain dependency).
 *
 * Слой: Domain (порт).
 *
 * ## Зачем
 * Доменная логика не должна напрямую вызывать `Math.random()`, потому что:
 * - это делает поведение недетерминированным (сложно воспроизводить баги),
 * - это затрудняет тестирование (нельзя “зафиксировать” рандом),
 * - это усложняет будущие реплеи/seeded runs.
 *
 * Поэтому домен принимает RNG как зависимость (инъекция через application/app).
 */
export type Rng = {
  /**
   * Возвращает число в диапазоне [0, 1).
   * Реализация может быть seeded (в будущем), но контракт остаётся простым.
   */
  nextFloat: () => number;
};


